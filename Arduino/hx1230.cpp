//
// SPI 3-wire transfers the D/C bit first, then 8-bits MSB first
// and latches the bits on the falling edge of the clock
// (idle low)
//
void bb_spi_transfer(byte *pBuf, byte bLen, byte bDC)
{
byte i, b, c;
   if (iCSPin != -1)
      digitalWrite(iCSPin, LOW);
   for (i=0; i<bLen; i++)
   {
      c = *pBuf++;
      // Write D/C bit
      digitalWrite(iDataPin, bDC); // set data bit (MSB first)
      digitalWrite(iClockPin, HIGH); // toggle clock
      digitalWrite(iClockPin, LOW); // high->low transition latches data
      
      for (b=0; b<8; b++)
      {
#ifdef __AVR_ATtiny85__
         if (c & 0x80)
            bitSet(PORTB, iDataPin); // faster to do direct bit manipulation on the ATtiny85
         else                       // than to call digitalWrite()
            bitClear(PORTB, iDataPin);
         bitSet(PORTB, iClockPin);
         bitClear(PORTB, iClockPin);
#else
         digitalWrite(iDataPin, (c & 0x80)); // set data bit (MSB first)
         digitalWrite(iClockPin, HIGH); // toggle clock
         digitalWrite(iClockPin, LOW); // high->low transition latches data
#endif
         c <<= 1; // shift over data
      } // for each bit
   } // for each byte
   if (iCSPin != -1)
      digitalWrite(iCSPin, HIGH);
} /* bb_spi_transfer() */

